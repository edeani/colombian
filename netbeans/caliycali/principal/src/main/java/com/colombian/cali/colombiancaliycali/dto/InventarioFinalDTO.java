public class InventarioFinalDTO {
    private Long codigo;
    private String producto;
    private Long inicial;
    private Long compras;
    private Long ventas;
    private Long averias;
    private Long ifinal;
    private Long fisico;
    private Long diferencia;

    /**
     * @return the codigo
     */
    public Long getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(Long codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the producto
     */
    public String getProducto() {
        return producto;
    }

    /**
     * @param producto the producto to set
     */
    public void setProducto(String producto) {
        this.producto = producto;
    }

    /**
     * @return the inicial
     */
    public Long getInicial() {
        return inicial;
    }

    /**
     * @param inicial the inicial to set
     */
    public void setInicial(Long inicial) {
        this.inicial = inicial;
    }

    /**
     * @return the compras
     */
    public Long getCompras() {
        return compras;
    }

    /**
     * @param compras the compras to set
     */
    public void setCompras(Long compras) {
        this.compras = compras;
    }

    /**
     * @return the ventas
     */
    public Long getVentas() {
        return ventas;
    }

    /**
     * @param ventas the ventas to set
     */
    public void setVentas(Long ventas) {
        this.ventas = ventas;
    }

    /**
     * @return the averias
     */
    public Long getAverias() {
        return averias;
    }

    /**
     * @param averias the averias to set
     */
    public void setAverias(Long averias) {
        this.averias = averias;
    }

    /**
     * @return the ifinal
     */
    public Long getIfinal() {
        return ifinal;
    }

    /**
     * @param ifinal the ifinal to set
     */
    public void setIfinal(Long ifinal) {
        this.ifinal = ifinal;
    }

    /**
     * @return the fisico
     */
    public Long getFisico() {
        return fisico;
    }

    /**
     * @param fisico the fisico to set
     */
    public void setFisico(Long fisico) {
        this.fisico = fisico;
    }

    /**
     * @return the diferencia
     */
    public Long getDiferencia() {
        return diferencia;
    }

    /**
     * @param diferencia the diferencia to set
     */
    public void setDiferencia(Long diferencia) {
        this.diferencia = diferencia;
    }
    
    
    
}
